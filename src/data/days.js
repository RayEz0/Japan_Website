/** Slugify a place name into a URL-safe key */
export function toPlaceKey(dayNum, placeName) {
  const slug = placeName
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/(^-|-$)/g, '')
  return `day${dayNum}-${slug}`
}
